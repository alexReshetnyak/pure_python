print('-----------------------COMPLEX_NUMBERS-------------------------------')
print('complex(4, 2):', complex(4, 2)) # (4+2j - a comlex number (not int or float)

print('-----------------------BINARY-------------------------------')
print('bin(5):', bin(5)) # 0b101 - binary number (5 = 101)
print("int('0b101')", int('0b101', 2)) # 5 (base 2 for binary values)
