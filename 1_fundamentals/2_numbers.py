print('---------------------------NUMBERS--------------------------------')
# Numbers
print('1 + 2:', 1 + 2)  # 3
print('type(2):', type(2))  # int
print('type(8 / 44):', type(8 / 44))  # float
print('type(20.9 + 1.1):', type(20.9 + 1.1))  # float
print('2**3:', 2**3)  # 8

# Double divide (square root)
print('4 // 2:', 4 // 2)  # 2
print('9 // 3:', 9 // 3)  # 3

# module
print('6 % 4:', 6 % 4)  # 2

print('-----------------------MATH_FUNCTIONS-------------------------------')

print('round(3.1):', round(3.1))  # 3
print('abs(-20):', abs(-20))  # 20 , absolute numbers (positive)

print('-----------------------OPERATOR_PRECEDENCE----------------------------')
print('20 + 3 * 4:', 20 - 3 * 2)  # 14
# ()
# **
# * /
# + -

print('-----------------------COMPLEX_NUMBERS---------------------------')
# (4+2j - a comlex number (not int or float)
print('complex(4, 2):', complex(4, 2))

print('-----------------------BINARY-------------------------------')
print('bin(5):', bin(5))  # 0b101 - binary number (5 = 101)
print("int('0b101')", int('0b101', 2))  # 5 (base 2 for binary values)
